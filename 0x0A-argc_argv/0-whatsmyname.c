#include <stdio.h>

/*
 * main - Entry point of the program
 * @argc: Prints the number of arguments used
 * @argv: An array of pointers to the arguments used
 *
 * Return: Always returns 0
 */

int main(int  __attribute__ ((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
